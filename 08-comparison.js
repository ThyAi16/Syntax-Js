// Toán tử so sánh, trả về true -> đúng, false -> sai
const a = 3;
const b = 7;

console.log(a > b);
console.log(a < b);
console.log(a == b);// So sánh giá trị không so sánh về kiểu dữ liệu
console.log(a === b);// So sánh giá trị và kiểu dữ liệu
console.log(a >= b);
console.log(a <= b);
console.log(a != b);// so sánh khác , so sánh về giá trị
console.log(a !== b);// so sánh khác, so sánh về giá trị và kiểu dữ liệu

